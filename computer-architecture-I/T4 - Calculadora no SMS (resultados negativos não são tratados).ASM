START:
	MOV DL, C0
	CALL 85; LER NUMERO 1
	CALL 85; LER NUMERO 2

OPERATION:
	IN 00; DIGITAR A OPERAÇÃO
	CMP AL, 2B; SOMA
	JZ CALCULATION
	CMP AL, 2D; SUBTRAÇÃO
	JZ CALCULATION
	CMP AL, 2A; MULTIPLICAÇÃO
	JZ CALCULATION
	CMP AL, 2F; DIVISÃO
	JZ CALCULATION
	JMP OPERATION

CALCULATION:
	MOV [DL], AL
	INC DL
	CALL 85; LER NUMERO 3
	CALL 85; LER NUMERO 4
	MOV AL, 3D
	MOV [DL], AL
	MOV CL, [C2]
	CMP CL, 2B
	JNZ SUBTRACT
	CALL 98
	ADD AL, CL
	JMP FINAL

SUBTRACT:
	CMP CL, 2D
	JNZ DIVISION
	CALL 98
	SUB AL, CL
	JMP FINAL

DIVISION:
	CMP CL, 2F
	JNZ MULTIPLY
	CALL 98
	DIV AL, CL
	JMP FINAL

MULTIPLY:
	CALL 98
	MUL AL, CL
	JMP FINAL

FINAL:
	PUSH AL
	MOD AL, 0A
	ADD AL, 30
	MOV [C8], AL
	POP AL
	DIV AL, 0A
	PUSH AL
	MOD AL, 0A
	ADD AL, 30
	MOV [C7], AL
	POP AL
	DIV AL, 0A
	ADD AL, 30
	MOV [C6], AL
	HALT

ORG 85
READING:
	IN 00; DIGITAR O NUMERO
	CMP AL, 30
	JS READING
	CMP AL, 40
	JNS READING
	MOV [DL], AL
	INC DL
	RET

ORG 98
	MOV AL, [C0]
	MOV BL, [C1]
	AND AL, 0F
	AND BL, 0F
	MUL AL, 0A
	ADD AL, BL
	MOV CL, [C3]
	MOV DL, [C4]
	AND CL, 0F
	AND DL, 0F
	MUL CL, 0A
	ADD CL, DL
	RET

END
		
	
	











	